package ransacPackage;

public class Point3D {
	
	// coordonn√©es d'un point
	private double X;
	private double Y;
	private double Z;
	
	// constructor of Point3D
	public Point3D(double x, double y, double z) {
		
		X = x;
		Y = y;
		Z = z;
	}
	
	// get value of X
	public double getX() {
		
		return X;
	}
	
	// get value of Y
	public double getY() {
		
		return Y;
	}
	
	// get value of Z
	public double getZ() {
		
		return Z;
	}

	
}
